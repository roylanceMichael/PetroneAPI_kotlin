package kr.co.byrobot.petroneapi

import android.test.mock.MockContext
import org.junit.Assert
import org.junit.Test

class PetroneBLETest {


  @Test
  fun simpleTest() {
    // arrange
    val context = MockContext()
    val p = PetroneBLE(context)
    val onSuccess = {s1: String, s2: String, i: Int ->
      println("success called! $s1  $s2 $i")
    }

    val onFailure = { error: String ->
      println("error called: $error")
    }
    // act
    p.scan(onSuccess, onFailure)

    // assert
    Assert.assertTrue(true)
  }
}